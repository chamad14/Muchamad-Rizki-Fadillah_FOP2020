#include <iostream>
using namespace std;

int main() {

int a = 12;
int b = 5;

// addition
cout << "12 + 5 =" << a + b << endl;

//substraction
cout << "12 - 5 =" << a - b << endl;

//multiplication
cout << "12 * 5 =" << a * b << endl;

//division
cout << "12 / 5 =" << a / b << endl;

//remainder
cout << "12 % 5 =" << a % b << endl;

return 0;

}