#include <iostream>
using namespace std;

int main() {

int a = 7;
int b = 4;

//before swapping
cout << "before swapping" << endl;
cout << "a =" << a << endl;
cout << "b =" << b << endl;

//after swapping
cout << "after swapping" << endl;
cout << "a =" << b << endl;
cout << "b =" << a << endl;

return 0;

}